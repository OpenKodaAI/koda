type BootstrapExchangeResponse = {
  ok: boolean;
  registration_token: string;
  expires_at?: string | null;
};

type DashboardAuthResponse = {
  ok: boolean;
  auth?: ControlPlaneAuthStatus | null;
  operator?: {
    username?: string | null;
    email?: string | null;
    display_name?: string | null;
  } | null;
};

const SUPPORTED_BOOTSTRAP_AUTH_MODES: BootstrapAuthMode[] = ["api_key", "local"];

function providerSupportsBootstrap(
  provider: ControlPlaneOnboardingStatus["providers"][number],
): boolean {
  return provider.supported_auth_modes.some((mode) =>
    SUPPORTED_BOOTSTRAP_AUTH_MODES.includes(mode as BootstrapAuthMode),
  );
}

function suggestedProviderId(status: ControlPlaneOnboardingStatus | null): string {
  if (!status) {
    return "";
  }
  if (status.system.default_provider) {
    return status.system.default_provider;
  }
  const verifiedProvider = status.providers.find((provider) => provider.verified);
  if (verifiedProvider) {
    return verifiedProvider.provider_id;
  }
  const configuredProvider = status.providers.find((provider) => provider.configured);
  if (configuredProvider) {
    return configuredProvider.provider_id;
  }
  const bootstrapProvider = status.providers.find(providerSupportsBootstrap);
  return bootstrapProvider?.provider_id ?? status.providers[0]?.provider_id ?? "";
}

function suggestedAuthMode(
  provider: ControlPlaneOnboardingStatus["providers"][number] | undefined,
): BootstrapAuthMode {
  const supported = (provider?.supported_auth_modes ?? []).filter((mode) =>
    SUPPORTED_BOOTSTRAP_AUTH_MODES.includes(mode as BootstrapAuthMode),
  ) as BootstrapAuthMode[];
  if (supported.includes("api_key")) {
    return "api_key";
  }
  if (supported.includes("local")) {
    return "local";
  }
  return "api_key";
}

function formatReadyState(ready: boolean) {
  return ready ? "Ready" : "Needs attention";
}

function formatAgentHint(status: ControlPlaneOnboardingStatus | null) {
  if (!status) {
    return "Optional during first run";
  }
  if (status.agents.length === 0) {
    return "Optional during first run";
  }
  const readyAgents = status.agents.filter((agent) => agent.telegram_token_configured).length;
  return `${readyAgents}/${status.agents.length} connected`;
}

function joinUserIds(status: ControlPlaneOnboardingStatus | null) {
  return status?.system.allowed_user_ids.join(", ") ?? "";
}

function defaultAgentName(status: ControlPlaneOnboardingStatus | null) {
  const ownerName = status?.system.owner_name?.trim();
  if (ownerName) {
    return `${ownerName}'s Agent`;
  }
  return "Koda Agent";
}

function deriveSetupStep({
  hasOperatorSession,
  hasOwner,
  registrationToken,
}: {
  hasOperatorSession: boolean;
  hasOwner: boolean;
  registrationToken: string;
}): SetupStep {
  if (hasOperatorSession) {
    return "finish_platform";
  }
  if (!hasOwner && registrationToken.trim()) {
    return "register_owner";
  }
  if (!hasOwner) {
    return "setup_code";
  }
  return "login";
}

function stepTitle(step: SetupStep) {
  switch (step) {
    case "setup_code":
      return "Connect with a setup code";
    case "register_owner":
      return "Create the owner account";
    case "login":
      return "Sign in to the control plane";
    case "finish_platform":
      return "Finish platform setup";
  }
}

function stepDescription(step: SetupStep) {
  switch (step) {
    case "setup_code":
      return "Paste the one-time setup code generated by the installer or CLI. It is short-lived and easier to rotate than exposing the raw recovery token.";
    case "register_owner":
      return "Create the local owner account that will drive dashboard sessions, API tokens, and future operator management.";
    case "login":
      return "Use the local owner account to open a secure HTTP-only operator session in the dashboard.";
    case "finish_platform":
      return "Storage is already online. Configure access policy, verify the default provider, and optionally connect the first agent.";
  }
}

function stepPosition(step: SetupStep) {
  return SETUP_FLOW.findIndex((item) => item.step === step);
}

function isStepComplete(
  step: SetupStep,
  {
    registrationToken,
    hasOwner,
    hasOperatorSession,
    onboardingComplete,
  }: {
    registrationToken: string;
    hasOwner: boolean;
    hasOperatorSession: boolean;
    onboardingComplete: boolean;
  },
) {
  switch (step) {
    case "setup_code":
      return Boolean(registrationToken.trim()) || hasOwner || hasOperatorSession;
    case "register_owner":
      return hasOwner || hasOperatorSession;
    case "login":
      return hasOperatorSession;
    case "finish_platform":
      return onboardingComplete;
  }
}

function formatDestination(nextTarget?: string | null) {
  return nextTarget || "/control-plane";
}

function guidanceForStep(step: SetupStep) {
  switch (step) {
    case "setup_code":
      return "Keep the terminal open after `koda install`. If the code expired, run `koda auth issue-code` and continue here.";
    case "register_owner":
      return "This is the long-lived dashboard identity. You can change the display name and email later in the control plane.";
    case "login":
      return "Use the username or email you just created. After sign-in, the dashboard stores the operator session in an HTTP-only cookie.";
    case "finish_platform":
      return "Access policy and provider are the only required inputs here. Creating the first Telegram agent is optional.";
  }
}

